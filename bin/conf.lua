function love.conf(t)
    t.identity = nil                    -- Le nom du répertoire de sauvegarde (Texte)
    t.version = "0.10.2"                -- La version de LÖVE pour laquelle ce jeu a été conçu (Texte)
    t.console = false                   -- Attache une console (booléen, Windows uniquement)
    t.accelerometerjoystick = true      -- Active l'accelerometre sur iOS et Android en l'exposant comme un Joystick (booléen)
    t.externalstorage = false           -- True pour sauver les fichiers (ou lire depuis le répertoire de sauvegarde) sur unsupport externe (booléen) 
    t.gammacorrect = false              -- Active la correction Gammade l'affichage, si supporté par le système (booléen)
 
    t.window.title = "Donjon"         -- Le titre de la fenêtre (Texte)
    t.window.icon = nil                 -- Chemin de fichier vers une image a utiliser comme icone de fenêtre (Texte)
    t.window.width = 800                -- La largeur de la fenêtre (Nombre)
    t.window.height = 600               -- La hauteur de la fenêtre (Nombre)
    t.window.borderless = false         -- Retire tout les affichage de bordures de la fenêtre (booléen)
    t.window.resizable = false          -- Rend la fenêtre redimensionnable par l'utilisateur (booléen)
    t.window.minwidth = 1               -- Largeur minimale de la fenêtre si elle est redimentionnable (Nombre)
    t.window.minheight = 1              -- Hauteur minimale de la fenêtre si elle est redimentionnable (Nombre)
    t.window.fullscreen = false         -- Active le mode plein écran (booléen)
    t.window.fullscreentype = "desktop" -- Choix entre le mode plein écran "desktop" ou le mode plein écran "exclusive" (Texte)
    t.window.vsync = true               -- Active la synchronisation verticale (booléen) (Limite les FPS a la capacité de l'écran)
    t.window.msaa = 0                   -- Le nombre de samples à utiliser avec l'antialiasing (Nombre)
    t.window.display = 1                -- Numéro de l'écran qui affiche la fenêtre (Nombre)
    t.window.highdpi = false            -- Active le mode haute résolution des fenêtre sur les écrans Retina (booléen)
    t.window.x = nil                    -- Position en X du coin supérieur gauche de la fenêtre sur l'écran (Nombre)
    t.window.y = nil                    -- Position en Y du coin supérieur gauche de la fenêtre sur l'écran (Nombre)
 
    t.modules.audio = true              -- Active le module audio (booléen)
    t.modules.event = true              -- Active le module de gestion des events (booléen)
    t.modules.graphics = true           -- Active le module graphique (booléen)
    t.modules.image = true              -- Active le module image (booléen)
    t.modules.joystick = true           -- Active le module joystick (booléen)
    t.modules.keyboard = true           -- Active le module keyboard (booléen)
    t.modules.math = true               -- Active le module math (booléen)
    t.modules.mouse = true              -- Active le module mouse (booléen)
    t.modules.physics = true            -- Active le module physics (booléen)
    t.modules.sound = true              -- Active le module sound (booléen)
    t.modules.system = true             -- Active le module system (booléen)
    t.modules.timer = true              -- Active le module timer (booléen), La désactivation entraine un deltatime de 0 dans love.update
    t.modules.touch = true              -- Active le module touch (booléen)
    t.modules.video = true              -- Active le module video (booléen)
    t.modules.window = true             -- Active le module the window (booléen)
    t.modules.thread = true             -- Active le module thread (booléen)
end